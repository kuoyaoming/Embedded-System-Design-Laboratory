{
    SystemClock_Config();
	prvSetupHardware();
	__enable_irq();
    __enable_fault_irq();

	all_init();
	Display_init();
	setPageMode();
	clearDisplay();
	hal_gpt_delay_ms(1000);
	setTextXY(0, 0);
	putString("OLED Light Meter");
	setTextXY(1, 0);
	putString("Light Value=");
	uint32_t adc_data;
    while (1)
	{
		hal_adc_get_data_polling(HAL_ADC_CHANNEL_0, &adc_data);
		setTextXY(1, 12);
		putString("    ");
		setTextXY(1, 12);
		putFloat(adc_data, 0);
		
		printf("Light Value=%04d\r\n", adc_data);
		hal_gpt_delay_ms(1000);
	}
}